def search_wisdom(figure: str = "", domain: str = "") -> list:
    """检索智慧库，按人物名或领域匹配。

    Args:
        figure: 人名（支持中文或英文，部分匹配）
        domain: 领域关键词

    Returns:
        匹配的人物数据列表
    """
    results = []
    figure = figure.strip().lower()
    domain = domain.strip().lower()

    for entry in WISDOM_DATA:
        score = 0
        # 按人物名匹配
        if figure:
            if figure in entry["name"].lower() or figure in entry["name_en"].lower():
                score += 3
            elif any(figure in word.lower() for word in entry["name"].split()):
                score += 1

        # 按领域匹配
        if domain:
            for d in entry["domains"]:
                if domain in d.lower() or d.lower() in domain:
                    score += 2
                    break

        if figure and domain:
            if score >= 2:  # 至少匹配一个维度
                results.append(entry)
        elif figure and score >= 1:
            results.append(entry)
        elif domain and score >= 1:
            results.append(entry)

    if not figure and not domain:
        return WISDOM_DATA

    # 按匹配度排序
    return sorted(results, key=lambda r: sum(
        (figure in r["name"].lower() or figure in r["name_en"].lower()) * 3 +
        any(domain in d.lower() for d in r["domains"]) * 2
        for _ in [1]
    ), reverse=True) if results else []


def format_wisdom(figure_data: dict) -> str:
    """将某个人物的智慧数据格式化为可读文本。"""
    lines = []
    lines.append(f"## {figure_data['name']}（{figure_data['name_en']}）")
    lines.append(f"领域：{'、'.join(figure_data['domains'])}")
    lines.append(f"核心思想：{figure_data['summary']}")
    lines.append("")

    lines.append("### 核心理念")
    for title, content in figure_data["philosophies"]:
        lines.append(f"- **{title}**：{content}")

    lines.append("")
    lines.append("### 行动指南")
    for i, guide in enumerate(figure_data["action_guides"], 1):
        lines.append(f"{i}. {guide}")

    lines.append("")
    lines.append("### 经典语录")
    for q in figure_data["quotes"]:
        lines.append(f"> {q}")

    return "\n".join(lines)
